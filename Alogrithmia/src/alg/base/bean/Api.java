package alg.base.bean;

public class Api {
	int usage_id;
	int algorithm_id;
	String lang_type;
	String api_addr;
	String api_content;
	public int getUsage_id() {
		return usage_id;
	}
	public void setUsage_id(int usage_id) {
		this.usage_id = usage_id;
	}
	public int getAlgorithm_id() {
		return algorithm_id;
	}
	public void setAlgorithm_id(int algorithm_id) {
		this.algorithm_id = algorithm_id;
	}
	public String getLang_type() {
		return lang_type;
	}
	public void setLang_type(String lang_type) {
		this.lang_type = lang_type;
	}
	public String getApi_addr() {
		return api_addr;
	}
	public void setApi_addr(String api_addr) {
		this.api_addr = api_addr;
	}
	public String getApi_content() {
		return api_content;
	}
	public void setApi_content(String api_content) {
		this.api_content = api_content;
	}
}
